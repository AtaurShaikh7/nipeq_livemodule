procedure SP_FE_MAX_ESTIMATE_DATE(RESULTSET OUT SYS_REFCURSOR)

AS

MAX_HOLD_DATE DATE;

BEGIN



SELECT MAX(EFFECTIVE_DATE) INTO MAX_HOLD_DATE FROM FUND_HOLDINGS;



OPEN RESULTSET FOR 



SELECT MAX(EFFECTIVE_DATE) AS EFFECTIVE_DATE,MAX_HOLD_DATE AS HOLDING_DATE FROM KM_SECURITY_ESTIMATES;



END;