PROCEDURE SP_KM_INSERT_REPORT_ATTACHMENT (

    REPID     IN NUMBER,

    FILE_NAME IN VARCHAR2,

    FILE_TYPE IN VARCHAR2,

    FILE_TIMESTAMP IN VARCHAR2)

AS

BEGIN

  INSERT

  INTO KM_REPORT_ATTACHMENT

    (

      REPORT_ID,

      FILENAME,

      FILETYPE,

      FILETIMESTAMP,

      UPLOAD_DATE

    )

    VALUES

    (

      REPID,

      FILE_NAME,

      FILE_TYPE,

      FILE_TIMESTAMP,

      SYSDATE

    );

END SP_KM_INSERT_REPORT_ATTACHMENT;